import { Router } from "express";

import {
  getAllUnits,
  getUnitById,
  createUnit,
  updateUnit,
  deleteUnit
} from "../controllers/unit.controller";

const router = Router();

/*
|--------------------------------------------------------------------------
| GET
|--------------------------------------------------------------------------
*/

router.get("/", getAllUnits);

router.get("/:id", getUnitById);

/*
|--------------------------------------------------------------------------
| POST
|--------------------------------------------------------------------------
*/

router.post("/", createUnit);

/*
|--------------------------------------------------------------------------
| PUT
|--------------------------------------------------------------------------
*/

router.put("/:id", updateUnit);

/*
|--------------------------------------------------------------------------
| DELETE
|--------------------------------------------------------------------------
*/

router.delete("/:id", deleteUnit);

export default router;