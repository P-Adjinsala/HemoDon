import { Router } from "express";

import {
  getAllLogs,
  getLogById,
  createLog,
  updateLog,
  deleteLog
} from "../controllers/log.controller";

const router = Router();

/*
|--------------------------------------------------------------------------
| GET
|--------------------------------------------------------------------------
*/

router.get("/", getAllLogs);

router.get("/:id", getLogById);

/*
|--------------------------------------------------------------------------
| POST
|--------------------------------------------------------------------------
*/

router.post("/", createLog);

/*
|--------------------------------------------------------------------------
| PUT
|--------------------------------------------------------------------------
*/

router.put("/:id", updateLog);

/*
|--------------------------------------------------------------------------
| DELETE
|--------------------------------------------------------------------------
*/

router.delete("/:id", deleteLog);

export default router;