import { Router } from "express";

import {
  getAllRoles,
  getRoleById,
  createRole,
  updateRole,
  deleteRole
} from "../controllers/role.controller";

const router = Router();

/*
|--------------------------------------------------------------------------
| GET
|--------------------------------------------------------------------------
*/

router.get("/", getAllRoles);

router.get("/:id", getRoleById);

/*
|--------------------------------------------------------------------------
| POST
|--------------------------------------------------------------------------
*/

router.post("/", createRole);

/*
|--------------------------------------------------------------------------
| PUT
|--------------------------------------------------------------------------
*/

router.put("/:id", updateRole);

/*
|--------------------------------------------------------------------------
| DELETE
|--------------------------------------------------------------------------
*/

router.delete("/:id", deleteRole);

export default router;